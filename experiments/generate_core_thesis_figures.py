#!/usr/bin/env python3
"""Generate core thesis figures using matplotlib/seaborn.

This script intentionally overwrites the thesis figure files that were
previously generated by the legacy Pillow pipeline.
"""

from __future__ import annotations

import json
from pathlib import Path

import matplotlib.pyplot as plt
import seaborn as sns


ROOT = Path(__file__).resolve().parents[1]
FIGURES_DIR = ROOT / "figures"
COMPLEXITY_BUNDLE = ROOT / "experiments" / "complexity_validation_bundle.json"
BUNDLE_10 = ROOT / "experiments" / "streaming_wfc_experiment_bundle_10x10.json"
BUNDLE_20 = ROOT / "experiments" / "streaming_wfc_experiment_bundle_20x20.json"
ENDURANCE = ROOT / "experiments" / "demo_endurance_4dir_1000_chunks.json"


def load_json(path: Path) -> dict:
    with path.open("r", encoding="utf-8") as handle:
        return json.load(handle)


def fit_theta_cubic(size_values: list[int], time_values: list[float]) -> tuple[float, float, float]:
    # Least-squares fit for a*G^2 + b*G^3 + c.
    s22 = s23 = s2 = s33 = s3 = 0.0
    y2 = y3 = y1 = 0.0
    n = len(size_values)
    for g, t in zip(size_values, time_values):
        g2 = float(g * g)
        g3 = g2 * float(g)
        s22 += g2 * g2
        s23 += g2 * g3
        s2 += g2
        s33 += g3 * g3
        s3 += g3
        y2 += g2 * t
        y3 += g3 * t
        y1 += t

    matrix = [
        [s22, s23, s2, y2],
        [s23, s33, s3, y3],
        [s2, s3, float(n), y1],
    ]

    for i in range(3):
        pivot = max(range(i, 3), key=lambda r: abs(matrix[r][i]))
        matrix[i], matrix[pivot] = matrix[pivot], matrix[i]
        div = matrix[i][i]
        if abs(div) < 1e-12:
            return 0.0, 0.0, 0.0
        for c in range(i, 4):
            matrix[i][c] /= div
        for r in range(3):
            if r == i:
                continue
            factor = matrix[r][i]
            for c in range(i, 4):
                matrix[r][c] -= factor * matrix[i][c]

    return matrix[0][3], matrix[1][3], matrix[2][3]


def generate_time_complexity_figure() -> Path:
    data = load_json(COMPLEXITY_BUNDLE)
    rows = data["controlled_comparison"]["rows"]

    sizes = [int(row["size"]) for row in rows]
    measured = [float(row["backtracking"]["time_ms"]["mean"]) for row in rows]
    best_measured = [float(row["backtracking"]["time_ms"]["min"]) for row in rows]
    worst_measured = [float(row["backtracking"]["time_ms"]["max"]) for row in rows]

    g2 = [float(g * g) for g in sizes]
    denom = sum(v * v for v in g2)
    k2 = (sum(v * t for v, t in zip(g2, worst_measured)) / denom) if denom > 0 else 0.0
    o2_ref = [k2 * v for v in g2]

    k_omega = min((t / v) for t, v in zip(best_measured, g2) if v > 0)
    omega_ref = [k_omega * v for v in g2]

    a2, b3, c0 = fit_theta_cubic(sizes, measured)
    theta_fit = [a2 * (g * g) + b3 * (g * g * g) + c0 for g in sizes]

    fig, ax = plt.subplots(figsize=(10, 6), dpi=220)
    ax.plot(sizes, measured, marker="o", linewidth=2.4, color="#2a9d8f", label="Measured average")
    ax.plot(sizes, o2_ref, marker="s", linewidth=2.0, color="#5b6675", label="Scaled O(G^2) reference")
    ax.plot(sizes, theta_fit, marker="^", linewidth=2.0, color="#8c2f39", label="Fitted Theta average model")
    ax.plot(sizes, omega_ref, marker="D", linewidth=2.0, color="#8b7752", label="Scaled Omega(G^2) reference")

    ax.set_title("Measured vs theoretical time-complexity curves", fontweight="bold")
    ax.set_xlabel("Chunk size G")
    ax.set_ylabel("Solve time (ms)")
    ax.set_xticks(sizes)
    ax.grid(True, alpha=0.25)
    ax.legend(frameon=True)

    out = FIGURES_DIR / "fig_time_complexity_validation.png"
    fig.tight_layout()
    fig.savefig(out, bbox_inches="tight")
    plt.close(fig)
    return out


def generate_streaming_timing_figure() -> Path:
    b10 = load_json(BUNDLE_10)
    b20 = load_json(BUNDLE_20)

    size_labels = ["10x10", "20x20"]
    x = [10, 20]
    y = [
        float(b10["streaming_scenarios"]["rows"][0]["avg_generation_time_ms"]),
        float(b20["streaming_scenarios"]["rows"][0]["avg_generation_time_ms"]),
    ]

    fig, ax = plt.subplots(figsize=(9.5, 5.4), dpi=220)
    ax.plot(x, y, marker="o", linewidth=2.8, color="#5b6675", label="Streaming mean")
    ax.axhline(16.0, linestyle="--", color="#b03a2e", linewidth=2.0, label="16 ms target")

    for xi, yi, label in zip(x, y, size_labels):
        ax.text(xi, yi + 0.35, f"{label}: {yi:.2f} ms", ha="center", va="bottom", fontsize=10)

    ax.set_title("Streaming generation-time gate", fontweight="bold")
    ax.set_xlabel("Chunk size")
    ax.set_ylabel("Generation time (ms)")
    ax.set_xticks(x)
    ax.set_xticklabels(size_labels)
    ax.set_yscale("log")
    ax.grid(True, which="both", alpha=0.25)
    ax.legend(frameon=True)

    out = FIGURES_DIR / "fig_streaming_timing_growth.png"
    fig.tight_layout()
    fig.savefig(out, bbox_inches="tight")
    plt.close(fig)
    return out


def generate_endurance_latency_figure() -> Path:
    data = load_json(ENDURANCE)
    metrics = [
        float(data["empirical_time"]["avg_generation_time_ms"]),
        float(data["empirical_time"]["p95_generation_time_ms"]),
        float(data["empirical_time"]["max_generation_time_ms"]),
    ]
    labels = ["Mean", "P95", "Max"]

    fig, ax = plt.subplots(figsize=(9.5, 5.4), dpi=220)
    palette = ["#2a9d8f", "#34699a", "#8b7752"]
    bars = sns.barplot(x=labels, y=metrics, hue=labels, palette=palette, dodge=False, legend=False, ax=ax)

    for bar, value in zip(bars.patches, metrics):
        ax.text(
            bar.get_x() + bar.get_width() / 2,
            bar.get_height() + 0.03,
            f"{value:.2f}",
            ha="center",
            va="bottom",
            fontsize=10,
        )

    ax.set_title("Latency profile for selected live configuration", fontweight="bold")
    ax.set_xlabel("Latency metric")
    ax.set_ylabel("Generation time (ms)")
    ax.grid(axis="y", alpha=0.25)

    out = FIGURES_DIR / "fig_endurance_latency_profile.png"
    fig.tight_layout()
    fig.savefig(out, bbox_inches="tight")
    plt.close(fig)
    return out


def main() -> None:
    FIGURES_DIR.mkdir(parents=True, exist_ok=True)
    sns.set_theme(style="whitegrid", context="talk")

    outputs = [
        generate_time_complexity_figure(),
        generate_streaming_timing_figure(),
        generate_endurance_latency_figure(),
    ]

    rel = [str(path.relative_to(ROOT)) for path in outputs]
    print(json.dumps({"generated": rel}, indent=2))


if __name__ == "__main__":
    main()
